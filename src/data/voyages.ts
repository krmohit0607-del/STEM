/**
 * Shared voyage dataset — the single source of truth that links every
 * page together.
 *
 * The Fleet List View (`/main`) renders one grid row per voyage. Clicking
 * a vessel navigates to `/voyage?voyage=<id>` and stores the selection
 * (see `selectedVoyage.ts`). The Voyage Details, Interim Dashboard,
 * Tracksheet (bottom panel) and Route Explorer / Simulator pages all
 * resolve the same `Voyage` by id, so the whole shell stays focused on
 * the vessel the user picked.
 *
 * Replace `VOYAGES` with the real `/api/voyage/list` payload when the
 * endpoint is exposed for the React app — keep the `id` stable so the
 * cross-page linking keeps working.
 */

export type Priority = 'HIGH' | 'MEDIUM' | 'LOW';

export interface Voyage {
  /** Stable voyage / order id used for cross-page linking. */
  id: string;

  // --- Operational fields (Fleet List grid + KPIs) -------------------
  priority: Priority;
  dueLt: number;
  dueUtc: number;
  remaining: string;
  vessel: string;
  pic: string;
  client: string;
  service: string;
  status: string;
  portFrom: string;
  portTo: string;
  eta: string;
  lastNoon: string;
  wx: string;
  int: string;
  eov: string;
  opt: string;
  openTasks: number;
  tags: string;
  aiAlert: string;
  health: number;
  handoverNote: string;
  open: string;

  // --- Vessel particulars (Interim Dashboard / Tracksheet) -----------
  imo: string;
  vesselType: string;
  flag: string;
  dwt: string;
  built: number;
  loa: string;
  beam: string;
  enginePower: string;

  // --- Commercial / routing (Voyage Details / Route pages) -----------
  clientEmail: string;
  price: number;
  pricingBasis: string;
  ecdisModel: string;
  interimPort: string;
  etdDisplay: string;
  etdIso: string;
  routeRef: string;
  cpSpeed: number;
  cpCons: number;
  instSpeed: number;
  instCons: number;
  costPerDay: number;
  foCost: number;
  goCost: number;
  euaCost: number;
  /** Deterministic seed used to vary the per-vessel sample tables. */
  seed: number;
}

