/** Build rows from compact tuples: [distance, current|null, weather, dtw, margin]. */
function rows(
  data: [number, number | null, number, number, number][],
): MarginRow[] {
  return data.map((d, i) => ({
    month: M[i],
    distance: d[0],
    currentEffect: d[1] === null ? undefined : d[1],
    weatherEffect: d[2],
    distanceThroughWater: d[3],
    seaMargin: d[4],
  }));
}

