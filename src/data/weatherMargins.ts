/**
 * Weather Margin route data, transcribed from the True North Marine
 * "Weather Margin" route-analysis document.
 *
 * Each route (area) has an assumptions / discussion write-up (where the
 * source provided one), a schematic map path (built from `PORTS`) and the
 * month-by-month Estimated Sea Margins table.
 *
 *   Distance Through Water = geographical distance + weather factor + current factor
 */

export interface MarginRow {
  month: string;
  /** Charted / geographical distance (nm). */
  distance: number;
  /** Current effect (nm). Undefined for charted-distance-only routes. */
  currentEffect?: number;
  /** Weather effect (nm) — always adverse (negative). */
  weatherEffect: number;
  /** Distance through water (nm). */
  distanceThroughWater: number;
  /** Sea margin (%). */
  seaMargin: number;
}

export interface WeatherMarginRoute {
  id: string;
  name: string;
  asOf?: string;
  hasCurrentEffect: boolean;
  /** Keys into `PORTS` describing the schematic route drawn on the map. */
  via: string[];
  assumptions?: string[];
  discussion?: string[];
  distances?: string[];
  rows: MarginRow[];
}

